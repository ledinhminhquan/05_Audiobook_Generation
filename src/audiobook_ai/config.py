"""Typed configuration + YAML loader for the Audiobook Generation system.

Single source of truth for the text-normalization corpus, the trainable TN
model, the TTS backends, audio post-processing, document parsing, agent
thresholds and serving. Paths come from environment variables so nothing is
hard-coded (required by the assignment).

Environment overrides
---------------------
* ``AUDIOBOOK_AI_ARTIFACTS_DIR`` – base for data/models/runs (Drive on Colab)
* ``AUDIOBOOK_AI_DATA_DIR``      – datasets / generated corpus cache
* ``AUDIOBOOK_AI_MODEL_DIR``     – trained models
* ``AUDIOBOOK_AI_RUN_DIR``       – eval/benchmark/analysis JSON
* ``AUDIOBOOK_AI_OUTPUT_DIR``    – generated audiobooks (wav/mp3/m4b/srt)
* ``HF_HOME``                    – HuggingFace cache
* ``AUDIOBOOK_AI_LLM_API_KEY``   – optional key for the LLM-reasoning fallback brain
"""

from __future__ import annotations

import os
from dataclasses import asdict, dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional

import yaml


def _env(key: str, default: Optional[str] = None) -> Optional[str]:
    v = os.environ.get(key)
    return v if v not in (None, "") else default


def artifacts_dir() -> Path:
    return Path(_env("AUDIOBOOK_AI_ARTIFACTS_DIR", "artifacts")).expanduser()


def data_dir() -> Path:
    return Path(_env("AUDIOBOOK_AI_DATA_DIR", str(artifacts_dir() / "data"))).expanduser()


def model_dir() -> Path:
    return Path(_env("AUDIOBOOK_AI_MODEL_DIR", str(artifacts_dir() / "models"))).expanduser()


def run_dir() -> Path:
    return Path(_env("AUDIOBOOK_AI_RUN_DIR", str(artifacts_dir() / "runs"))).expanduser()


def output_dir() -> Path:
    return Path(_env("AUDIOBOOK_AI_OUTPUT_DIR", str(artifacts_dir() / "outputs"))).expanduser()


